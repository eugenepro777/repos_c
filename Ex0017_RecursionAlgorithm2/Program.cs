void PrintImage(int[,] image)// принимает на вход нашу матрицу
// matrix.GetLength(0) - задаём до максимума строк
// matrix.GetLength(1) - задаём до максимума столбцов
{
    for (int i = 0; i < image.GetLength(0); i++) // цикл по строкам
    {
        for (int j = 0; j < image.GetLength(1); j++)//цикл по столбцам
        {
            if (image[i, j] == 0) Console.Write($" "); //если за границами
            else Console.Write($"+");

        }
        Console.WriteLine();//переход на следующую строку
    }
}

void FillImage(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row - 1, col); // рекурсия - метод вызывает сам себя
        FillImage(row, col - 1); // рекурсия
        FillImage(row + 1, col); // рекурсия
        FillImage(row, col + 1); // рекурсия
    }
}